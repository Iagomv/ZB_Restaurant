class Persona {
	constructor(id_personal, nombre, apellidos, dni, edad, fecha_inicio, fecha_fin_contrato, horas_semanales, rol, imagen) {
		this.id_personal = id_personal
		this.nombre = nombre
		this.apellidos = apellidos
		this.dni = dni
		this.edad = edad
		this.fecha_inicio = fecha_inicio
		this.fecha_fin_contrato = fecha_fin_contrato
		this.horas_semanales = horas_semanales
		this.rol = rol
		this.imagen = imagen
	}
}
